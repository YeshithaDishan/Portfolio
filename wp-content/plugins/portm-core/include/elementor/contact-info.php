                'default' => esc_html__('Direct email', 'tpcore'),
                'label_block' => true,
            ]
        );

        $repeater->add_control(
            'tp_description',
            [
                'label' => esc_html__('Description', 'tpcore'),
                'description' => tp_get_allowed_html_desc( 'intermediate' ),
                'type' => \Elementor\Controls_Manager::TEXTAREA,
                'default' => 'Become an industry expert',
                'label_block' => true,
            ]
        );         

        $repeater->add_control(
            'tp_btn_text', [
                'label' => esc_html__('Button Text', 'tpcore'),
                'type' => \Elementor\Controls_Manager::TEXT,
                'default' => esc_html__('Read Articles', 'tpcore'),
                'label_block' => true,
            ]
        );

        $repeater->add_control(
            'tp_contact_link',
            [
                'label' => esc_html__('Description CTA', 'tpcore'),
                'description' => tp_get_allowed_html_desc( 'intermediate' ),
                'type' => \Elementor\Controls_Manager::TEXTAREA,
                'default' => '#',
                'label_block' => true,
            ]
        ); 

        $this->add_control(
            'tp_list',
            [
                'label' => esc_html__('Services - List', 'tpcore'),
                'type' => \Elementor\Controls_Manager::REPEATER,
                'fields' => $repeater->get_controls(),
                'default' => [
                    [
                        'tp_title' => esc_html__('My Articles', 'tpcore'),
                    ],
                    [
                        'tp_title' => esc_html__('FAQs', 'tpcore')
                    ],
                    [
                        'tp_title' => esc_html__('Direct email', 'tpcore')
                    ]
                ],
                'title_field' => '{{{ tp_title }}}',
            ]
        );

        $this->add_group_control(
            Group_Control_Image_Size::get_type(),
            [
                'name' => 'thumbnail', // // Usage: `{name}_size` and `{name}_custom_dimension`, in this case `thumbnail_size` and `thumbnail_custom_dimension`.
