	/**
    * Render the widget output on the frontend.
    *
    * Written in PHP and used to generate the final HTML.
    *
    * @since 1.0.0
    *
    * @access protected
    */
	protected function render() {
		$settings = $this->get_settings_for_display();

			$this->add_render_attribute('title_args', 'class', 'cs_form_title cs_font_36 cs_semi_bold m-0');

        ?>


      <!--Start Iconbox Section-->
        <section>
          <div class="container">
            <div class="cs_iconbox_wrap">
              <div class="row justify-content-md-center cs_gap_40">
                <?php foreach ($settings['tp_list'] as $item) : ?>
                <div class="col-xl-4 col-md-6">
                  <div class="cs_iconbox cs_style_4 cs_radius_10 d-flex cs_gap_30 contact-iconbox-area">
                    <div class="cs_iconbox_icon">
                        <?php if (!empty($item['tp_features_image']['url'])): ?>
                            <img src="<?php echo $item['tp_features_image']['url']; ?>" alt="<?php echo get_post_meta(attachment_url_to_postid($item['tp_features_image']['url']), '_wp_attachment_image_alt', true); ?>">
                        <?php endif; ?>  
                    </div>
                    <div class="cs_iconbox_info">
                    <?php if (!empty($item['tp_title' ])): ?>
                      <h4 class="cs_iconbox_title cs_font_24 cs_semi_bold"><?php echo tp_kses($item['tp_title' ]); ?></h4>
                      <?php endif; ?>
                    <?php if (!empty($item['tp_description' ])): ?>
                      <p class="cs_iconbox_text"><?php echo tp_kses($item['tp_description']); ?></p>
                    <?php endif; ?>
                    <?php if (!empty($item['tp_btn_text' ])): ?>
                      <a class="cs_accent_color_2 cs_accent_color_hover cs_text_btn" href="<?php echo esc_url( $item['tp_contact_link'] ); ?>"><?php echo tp_kses($item['tp_btn_text' ]); ?></a>
                    <?php endif; ?>
                    </div>
                  </div>
                </div>
                <?php endforeach; ?>
              </div>
            </div>
          </div>
        </section>
        <!--End Iconbox Section-->

        <!--Start Contact Section-->
        <section class="position-relative">
          <div class="cs_service_shape_1 position-absolute">
            <img src="<?php echo get_template_directory_uri(); ?>/assets/img/service_shape_1.svg" alt="">
          </div>
          <div class="cs_height_115 cs_height_lg_45"></div>
          <div class="container">
            <div class="row">
              <div class="col-lg-8 offset-lg-2">
                <?php if (!empty( $settings['tg_section_title_show'] )) : ?>
                      <?php
                        if ( !empty($settings['tg_title']) ) :
                            printf( '<%1$s %2$s>%3$s</%1$s>',
                                tag_escape( $settings['tg_title_tag'] ),
                                $this->get_render_attribute_string( 'title_args' ),
                                tp_kses( $settings['tg_title'] )
                            );
                        endif;
                    ?>                    
                <?php if (!empty($settings['tg_sub_title'])) : ?>
                    <p class="cs_form_text"><?php echo esc_html( $settings['tg_sub_title'] ); ?></p>
                <?php endif; ?>                    
                <?php endif; ?>
                <div class="cs_height_5 cs_height_lg_5"></div>

                <?php echo do_shortcode( $settings['contact_shortCode'] ) ?>

              </div>
            </div>
          </div>
          <div class="cs_height_150 cs_height_lg_80"></div>
        </section>
        <!--End Contact Section-->

    <?php
	}
